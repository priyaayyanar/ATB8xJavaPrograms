package sept.ex_13092024;

public class Lab025_Constants {

    public static void main(String[] args) {

        //Constants are variables
        //Whose value can not be changed once they are assigned
        //"final" is a data type and the variable name always should be on capital case
        final float PIE = 3.1456f;
        System.out.println(PIE);
    }
}

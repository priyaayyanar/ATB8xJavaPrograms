package sept.ex_25092024;

public class Lab110_while_loop {
    public static void main(String[] args) {
        //while loop
        // 3 parts -> A, B, C
        //A -> init
        //B -> condition
        //C -> Inc / Dec
        //Same like for loop. The difference is 3 parts are divided. init should be outside of the loop

        int i = 0; //A
        while (i <= 10){ //B
            System.out.println(i);
            i++; //C
        }
    }
}

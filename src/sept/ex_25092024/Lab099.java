public class Lab099 {
    public static void main(String[] args) {

        // we can print a,b,c,d to z by using char type
        for (char c = 'a'; c<= 'z'; c++){
            System.out.println(c);
        }
        for (char c = 'A'; c<= 'Z'; c++){
            System.out.println(c);
        }
    }
}

package sept.ex_16092024;

public class Lab030_ArithmeticOperator {
    public static void main(String[] args) {
        //ArithmeticOperators -> "+,-,*,/
        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % -> Modulus operator -> provides remainder
        System.out.println(67%13);
    }
}

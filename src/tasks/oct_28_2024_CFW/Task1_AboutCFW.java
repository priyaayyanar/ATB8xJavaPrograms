public class Task1_AboutCFW {
    /* Collection FrameWork
        -> The Java Collection Framework is a set of classes and Interfaces
           that implement commonly reusable Collection data structures.
        -> Although refers to as a Framework, it works as a Library.
        -> The CFW provides both the Interfaces that define various collections
       and classes which implement them.

       Advantages of CFW:
        -> Reduces Programming effort: by providing DS and Algorithms
        -> Increases Performance : by providing high-performance implementations of DataStructures and Algorithms (DSA)
        -> Provides interoperability between unrelated APIs : by establishing a common language to pass collections back and forth.
        -> Reduces the effort required to learn APIs: by requiring us to learn multiple ad-hoc collection APIs.
        -> Reduces the effort required to design and implement APIs.
        -> Fosters s/w reuse.

       CFW consists of :
        -> Collection interfaces : diff types of collections such as List, Set, Queue and Maps. Basis of the FW.
        -> General Purpose Implementations : Primary implementations of Collection Interfaces.
        -> Legacy Implementations : Classes from earlier releases such as "Vector and HashTable"
        -> Special Purpose Implementations : These implementations display nonstandard performance characteristics, usage restrictions, or behavior.
        -> Concurrent Implementations : Implementations designed for highly concurrent use.
        -> Wrapper Implementations : Add functionality, such as synchronization, to other implementations.
        -> Convenience Implementations : High-performance "mini-implementations" of the collection interfaces.
        -> Abstract Implementations : Partial implementations of the collection interfaces to facilitate custom implementations.
        -> Algorithms : Static methods that perform useful functions on collections, such as sorting a list.
        -> Infrastructures : Interfaces that provide essential support for the collection interfaces.
     */
}
